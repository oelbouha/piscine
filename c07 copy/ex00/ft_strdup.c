#include <stdlib.h>

int	ft_strlen(char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		i++;
	}
	i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	int		i;
	char	*dest;

	i = 0;
	dest = malloc(ft_strlen(src) * sizeof (char) + 1);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
	i++;
	}
	dest[i] = '\0';
	return (dest);
}
